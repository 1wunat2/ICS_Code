/**
 *
 * @author nwu10
 */
public class LoopStructures {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        // WHILE
        int i = 0;
        while(i < 6){
            System.out.print(i);
            i++;
        }
        System.out.println("");
        
        //FOR
        for (int j = 0; j < 6; j++) {
            System.out.println(j);
        }
    }
    
}
